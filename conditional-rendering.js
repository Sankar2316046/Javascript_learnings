//learn about conditional rendering

var status = true;

if(status) //means status is true
{
    console.log("Status is true");
}
else{
    console.log("Status is false");
}

//this can be simplified as 

// syntax:

// condition ? "statement for true" : "statement for false";


status ? console.log("Status true from Conditional rendering") : console.log("Status false from Conditional rendering");  




